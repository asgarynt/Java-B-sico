public class SmartDevices {

    String marca;
    String modelo;
    String color;
    int releaseYear;
    String oS;

    public SmartDevices() {
    }

    public SmartDevices(String marca, String modelo, String color, int releaseYear, String oS) {
        this.marca = marca;
        this.modelo = modelo;
        this.color = color;
        this.releaseYear = releaseYear;
        this.oS = oS;
    }
}
